package Chapter1;

/**
 * Program displays area and perimeter of a circle
 *
 * @author Elijah Hamlin
 */
public class C1_8 {

    /**
     * Main Method
     *
     * @param args arguments from command line prompt
     */
    public static void main(String[] args) {
        System.out.println("Area of a circle with radius 5.5: ");
        System.out.println(5.5 * 5.5 * 3.14);

        System.out.println("The perimeter of that same circle is: ");
        System.out.println(2 * 5.5 * 3.14);
    }
}
